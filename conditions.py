def less_than_or_equal_to_zero(num1):
    result = num1 <= 0
    return result

print(less_than_or_equal_to_zero(5))
print(less_than_or_equal_to_zero(0))
print(less_than_or_equal_to_zero(-2))

def addition(x,y):
    result = x + y
    return result

print(addition(3,2))
print(addition(-3,-6))
print(addition(7,3))
